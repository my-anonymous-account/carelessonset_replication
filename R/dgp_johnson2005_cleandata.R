#' get the 'clean' sample correlation matrix from Johnson (2005)
#' 
#' To compute this sample correlation matrix, we take out all observations that were flagged by either antonyms, personal-reliability, or CODERS (at the liberal level of 1%). We use our own calculations (based on default settings in the \pkg{careless} package) and not the data that Johnson (2005) provides for the former two indices. We calculate the sample correlation matrix based on the remaining "clean" sample and round it to 2 decimal points (rounding to 1  decimal point would lead to loss of positive semidefiniteness). See \code{application/10johnson2005_cormat-for-sim.R} for details. Importantly, the correlation matrix is based on reverse-coded responses (so that within-scale correlations are expected to be all positive)
johnson05_cormat <- function()
{
  # load the correlation matrix (sorted by facets)
  #load("application/results/johnson2005_cleancormat.Rdata") # the Rdata file below is just that
  load("R/johnson2005_cleancormat.Rdata")
  
  # get the original order of the items (as presented to participants)
  item_order_original <- order(key_order)
  
  # update the item names
  colnames(cormat_clean_posword) <- rownames(cormat_clean_posword) <- key_membership[key_order]
  
  return(
    list(cormat_posword = cormat_clean_posword,
         item_order_ordrerd = key_order,
         item_order_original = item_order_original,
         item_keys_original = key_membership,
         item_facets_original = facets_membership,
         reverse_coded_original = which(key_sign == "-"))
  )
}


#' load the response marginals, following the same strategy as in \code{johnson05_cormat()}
johnson05_marginals <- function()
{
  # load the clean data: generated by `application/10johnson2005_cormat-for-sim.R`
  load("R/johnson2005_cleandata.Rdata")

